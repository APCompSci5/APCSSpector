package ch5;

public class MultipleSelection {
	public static void main(String args[]){
		
		int grade=85;
		if(grade >= 90){
			System.out.println("My grade is an A");
		}
		else if(grade < 90 && grade >= 80){
			System.out.println("My grade is a B");
		}
		else if (grade == 101){
			System.out.println("My grade is A++");
		
		}
	}

}
